function deleteFunctions(obj) {
    const functionKeys = [];
    for(const key in obj){
        if (typeof obj[key] === "function") {
            functionKeys.push(key);
        }
    }
    for(let i = 0; i < functionKeys.length; i++){
        delete obj[functionKeys[i]];
    }
}
function serialize1(obj, options) {
    options || (options = {
    });
    if (typeof options === 'number' || typeof options === 'string') {
        options = {
            space: options
        };
    }
    const functions = [];
    const regexps = [];
    const dates = [];
    const maps = [];
    const sets = [];
    const arrays = [];
    const undefs = [];
    const infinities = [];
    const bigInts = [];
    function replacer(key, value) {
        if (options.ignoreFunction) {
            deleteFunctions(value);
        }
        if (!value && value !== undefined) {
            return value;
        }
        const origValue = this[key];
        const type = typeof origValue;
        if (type === 'object') {
            if (origValue instanceof RegExp) {
                return '@__R-' + UID + '-' + (regexps.push(origValue) - 1) + '__@';
            }
            if (origValue instanceof Date) {
                return '@__D-' + UID + '-' + (dates.push(origValue) - 1) + '__@';
            }
            if (origValue instanceof Map) {
                return '@__M-' + UID + '-' + (maps.push(origValue) - 1) + '__@';
            }
            if (origValue instanceof Set) {
                return '@__S-' + UID + '-' + (sets.push(origValue) - 1) + '__@';
            }
            if (origValue instanceof Array) {
                const isSparse = origValue.filter(function() {
                    return true;
                }).length !== origValue.length;
                if (isSparse) {
                    return '@__A-' + UID + '-' + (arrays.push(origValue) - 1) + '__@';
                }
            }
        }
        if (type === 'function') {
            return '@__F-' + UID + '-' + (functions.push(origValue) - 1) + '__@';
        }
        if (type === 'undefined') {
            return '@__U-' + UID + '-' + (undefs.push(origValue) - 1) + '__@';
        }
        if (type === 'number' && !isNaN(origValue) && !isFinite(origValue)) {
            return '@__I-' + UID + '-' + (infinities.push(origValue) - 1) + '__@';
        }
        if (type === 'bigint') {
            return '@__B-' + UID + '-' + (bigInts.push(origValue) - 1) + '__@';
        }
        return value;
    }
    function serializeFunc(fn) {
        const serializedFn = fn.toString();
        if (IS_NATIVE_CODE_REGEXP.test(serializedFn)) {
            throw new TypeError('Serializing native function: ' + fn.name);
        }
        if (IS_PURE_FUNCTION.test(serializedFn)) {
            return serializedFn;
        }
        if (IS_ARROW_FUNCTION.test(serializedFn)) {
            return serializedFn;
        }
        const argsStartsAt = serializedFn.indexOf('(');
        const def = serializedFn.substr(0, argsStartsAt).trim().split(' ').filter((val)=>val.length > 0
        );
        const nonReservedSymbols = def.filter((val)=>{
            return RESERVED_SYMBOLS.indexOf(val) === -1;
        });
        if (nonReservedSymbols.length > 0) {
            return (def.indexOf('async') > -1 ? 'async ' : '') + 'function' + (def.join('').indexOf('*') > -1 ? '*' : '') + serializedFn.substr(argsStartsAt);
        }
        return serializedFn;
    }
    if (options.ignoreFunction && typeof obj === "function") {
        obj = undefined;
    }
    if (obj === undefined) {
        return String(obj);
    }
    let str;
    if (options.isJSON && !options.space) {
        str = JSON.stringify(obj);
    } else {
        str = JSON.stringify(obj, options.isJSON ? null : replacer, options.space);
    }
    if (typeof str !== 'string') {
        return String(str);
    }
    if (options.unsafe !== true) {
        str = str.replace(UNSAFE_CHARS_REGEXP, escapeUnsafeChars);
    }
    if (functions.length === 0 && regexps.length === 0 && dates.length === 0 && maps.length === 0 && sets.length === 0 && arrays.length === 0 && undefs.length === 0 && infinities.length === 0 && bigInts.length === 0) {
        return str;
    }
    return str.replace(PLACE_HOLDER_REGEXP, (match, backSlash, type, valueIndex)=>{
        if (backSlash) {
            return match;
        }
        if (type === 'D') {
            return "new Date(\"" + dates[valueIndex].toISOString() + "\")";
        }
        if (type === 'R') {
            return "new RegExp(" + serialize1(regexps[valueIndex].source) + ", \"" + regexps[valueIndex].flags + "\")";
        }
        if (type === 'M') {
            return "new Map(" + serialize1(Array.from(maps[valueIndex].entries()), options) + ")";
        }
        if (type === 'S') {
            return "new Set(" + serialize1(Array.from(sets[valueIndex].values()), options) + ")";
        }
        if (type === 'A') {
            return "Array.prototype.slice.call(" + serialize1(Object.assign({
                length: arrays[valueIndex].length
            }, arrays[valueIndex]), options) + ")";
        }
        if (type === 'U') {
            return 'undefined';
        }
        if (type === 'I') {
            return infinities[valueIndex];
        }
        if (type === 'B') {
            return "BigInt(\"" + bigInts[valueIndex] + "\")";
        }
        const fn = functions[valueIndex];
        return serializeFunc(fn);
    });
}
